ORG 0000H
MOV R5,#00H
MOV R0,#70H
MOV R1,#0B1H

JMP LOOP
START:
MOV R0,#70H
MOV R1,#0B6H
MOV R5,#00H

LOOP:
MOV A,#00001111B
ANL A,R0
JZ CONDITION1
JNZ CONDITION2


MAIN:
MOV P1,R0
CALL DELAY
MOV P1,R1
CALL DELAY
INC R5
CJNE R1,#0B0H,LOOP
CJNE R0,#70H,LOOP
JMP START



CONDITION1:
CJNE R5,#8AH,MAIN
MOV R5,#00H
MOV R0,#79H
DEC R1
JMP LOOP


CONDITION2:
CJNE R5,#8AH,MAIN
MOV R5,#00H
DEC R0
JMP LOOP

